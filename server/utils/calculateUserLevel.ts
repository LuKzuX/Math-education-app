export const calculateUserLevel = (total_xp: number): number => {
    let level = 0
    const xpThresholds = [
        30,   // 0
        50,   // 1
        70,   // 2
        100,  // 3
        150,  // 4
        210,  // 5
        280,  // 6
        360,  // 7
        450,  // 8
        550,  // 9
        700,  // 10
        880,  // 11  (+180)
        1090, // 12  (+210)
        1330, // 13  (+240)
        1600, // 14  (+270)
        1900, // 15  (+300)
        2230, // 16  (+330)
        2590, // 17  (+360)
        2980, // 18  (+390)
        3400, // 19  (+420)
        3850, // 20  (+450)
        4330, // 21  (+480)
        4840, // 22  (+510)
        5380, // 23  (+540)
        5950, // 24  (+570)
        6550, // 25  (+600)
        7180, // 26  (+630)
        7840, // 27  (+660)
        8530, // 28  (+690)
        9250, // 29  (+720)
        10000,// 30  (+750)
    ]
    for (let i = 0; i < xpThresholds.length; i++) {
        if (total_xp >= xpThresholds[i]) {
            level = i + 1;
        } else {
            break;
        }
    }
    return level
}